package org.esupportail.nfctag.web.wsrest.json;

public class JsonDamAuthKey {

    private String result;

    private String uid;

    private String damAuthKey;

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getDamAuthKey() {
        return damAuthKey;
    }

    public void setDamAuthKey(String damAuthKey) {
        this.damAuthKey = damAuthKey;
    }
}
